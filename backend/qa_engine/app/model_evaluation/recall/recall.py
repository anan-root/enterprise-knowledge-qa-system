def cosine_similarity_numpy(a, b):
    """使用NumPy计算余弦相似度"""
    dot_product = np.dot(a, b)
    norm_a = np.linalg.norm(a)
    norm_b = np.linalg.norm(b)

    if norm_a == 0 or norm_b == 0:
        return 0

    return dot_product / (norm_a * norm_b)


async def substring_match_evaluation(retrieved_chunks, answer_chunks, min_overlap_ratio=0.9):
    """
    答案片段的关键部分出现在检索结果中即计为命中
    """
    hits = 0
    overlaps = []
    if not isinstance(answer_chunks, list):
        answer_chunks = [answer_chunks]
    for answer in answer_chunks:
        answer_len = len(answer)
        is_hit = False

        for retrieved in retrieved_chunks:
            retrieved = ''.join(retrieved.split()).replace('$','').replace('\\','')
            answer = ''.join(answer.split())
            # 计算最长公共子串
            lcs_len = longest_common_substring(answer, retrieved)
            overlap_ratio = lcs_len / answer_len if answer_len > 0 else 0

            if overlap_ratio >= min_overlap_ratio:
                is_hit = True
                break
            else:
                embed_r = await get_embedding_api(retrieved)
                embed_a = await get_embedding_api(answer)
                similarity = cosine_similarity_numpy(embed_a, embed_r)
                if similarity > 0.85:
                    is_hit = True
                    overlap_ratio = max(similarity, min_overlap_ratio)
                    print(overlap_ratio)
                    print(retrieved)
                    break
                else:
                    retrieved_splits = retrieved.split('。')
                    for retrieved_split in retrieved_splits:
                        embed_r = await get_embedding_api(retrieved_split)
                        # embed_a = await get_embedding_api(answer)
                        similarity_split = cosine_similarity_numpy(embed_a, embed_r)
                        if similarity_split > 0.85:
                            is_hit = True
                            overlap_ratio = max(similarity_split, min_overlap_ratio)
                            print(overlap_ratio)
                            print(retrieved_split)
                            break
                if is_hit:
                    break

        # 在循环内添加调试输出
        if is_hit and overlap_ratio < 0.9:
            print(f"警告: hit 但 overlap 低! answer={answer[:50]}...")
            print(f"char_overlap={overlap_ratio}, similarity={similarity}")
            print(f"current_overlap={overlap_ratio}")
            break

        if is_hit:
            hits += 1
            overlaps.append(overlap_ratio)
        if not is_hit:
            overlaps.append(0)

    recall = hits / len(answer_chunks) if answer_chunks else 0
    return recall,overlaps


def longest_common_substring(str1: str, str2: str) -> int:
    """
    计算两个字符串的最长公共子串长度
    时间复杂度: O(m*n)，空间复杂度: O(m*n)
    """
    m, n = len(str1), len(str2)
    if m == 0 or n == 0:
        return 0

    # 创建DP表
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    max_length = 0

    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if str1[i - 1] == str2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
                max_length = max(max_length, dp[i][j])
            else:
                dp[i][j] = 0

    return max_length


async def recall_evaluation_single(user_question,retrieved_chunks, answer_chunks, min_overlap_ratio=0.9,n_final=None):

    if n_final is not None:
        retrieved_chunks = retrieved_chunks[:n_final]

    recall,overlaps = await substring_match_evaluation(retrieved_chunks, answer_chunks, min_overlap_ratio)

    return {'user_question':user_question,'recall':recall,'overlaps':overlaps,'answer_chunks':answer_chunks}




async def recall_evaluation_batch(q_r_a, min_overlap_ratio=0.9, n_final=None,max_concurrency=3):
    sem = Semaphore(max_concurrency)

    async def bounded_eval(item):
        async with sem:
            return await recall_evaluation_single(
                item['user_question'],
                item['retrieval_chunks'],
                item['answer_chunks'],
                min_overlap_ratio,
                n_final
            )

    tasks = [bounded_eval(i) for i in q_r_a]
    all_results = await asyncio.gather(*tasks)

    # ===== 统计逻辑（保持不变）=====
    all_overlaps = []
    all_recalls = []

    for result in all_results:
        all_overlaps.extend(result['overlaps'])
        all_recalls.append(result['recall'])

    all_overlaps = np.array(all_overlaps)
    all_recalls = np.array(all_recalls)

    final_result = {
        '片段级召回率': (
            f'{(sum(all_overlaps >= min_overlap_ratio) / len(all_overlaps)):.2%}, '
            f'找到了{sum(all_overlaps >= min_overlap_ratio)}/{len(all_overlaps)}个相关片段'
        ),
        '问题及召回率': (
            f'{sum(all_recalls == 1) / len(all_recalls):.2%}, '
            f'{sum(all_recalls == 1)}/{len(all_recalls)}个问题找到了全部片段'
        ),
        '平均召回率': f'{sum(all_recalls) / len(all_recalls):.2%}'
    }

    return final_result, all_results

